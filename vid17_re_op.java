public class vid17_re_op {
    public static void main(String[] args) {
        boolean a=true;
        boolean b=false;
        if(a && b){
            System.out.println("yes");
        }
        else{
            System.out.println("no");
        }
        System.out.println("Logical OR");
        boolean a1=true;
        boolean b1=false;
        if(a1 || b1){
            System.out.println("yes");
        }
        else{
            System.out.println("no");
        }
        System.out.println("Logical NOT");
        boolean a2=true;
        boolean b2=false;
        if(a2 != b2){
            System.out.println("yes");
        }
        else{
            System.out.println("no");
        }
    }
}
